export const getMunicipalitiesByState = (stateCode: string): Municipality[] => {
  return prMunicipalities.filter(municipality => municipality.stateCode === stateCode);
};

export const getMunicipalityByCode = (code: string): Municipality | undefined => {
  return prMunicipalities.find(municipality => municipality.code === code);
};

export const getMunicipalityOptions = (stateCode?: string) => {
  const municipalities = stateCode ? getMunicipalitiesByState(stateCode) : prMunicipalities;
  return municipalities.map(municipality => ({
    label: municipality.name,
    value: municipality.code
  }));
}; 